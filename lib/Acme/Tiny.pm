version 0.003

=head1 SYNOPSIS

    use Acme::Tiny;

=head1 DESCRIPTION

This module does the tiniest thing of all -- nothing!

Given all the recent releases of C<*::Tiny> modules, I felt like I needed to
get in on the action while there was still part of the namespace left.

